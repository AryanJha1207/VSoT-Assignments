arr = [1.2, 3.4, 4, 5, 6, 7.4]; #Creating an array with n elements
print(arr[2]) #accessing array element at 2nd index
print(arr[-5]) #accessing element at -5 index, it gives us the 5th element starting from last with the count 1 (i.e. 3.4 in this case)
arr.insert(1,"abc"); #inserting an element in the array at 1th index
print(arr); #Printing the whole array at once
